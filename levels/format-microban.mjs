import { readFileSync, writeFileSync } from 'node:fs';

const inputPath = new URL('./microbanText.html', import.meta.url);
const outputPath = new URL('./microban.js', import.meta.url);

const html = readFileSync(inputPath, 'utf8');
const preMatch = html.match(/<pre[^>]*>([\s\S]*?)<\/pre>/i);

if (!preMatch) {
  throw new Error('Could not find <pre> block in microbanText.html');
}

const source = preMatch[1]
  .replace(/\r\n?/g, '\n')
  .replace(/&lt;/g, '<')
  .replace(/&gt;/g, '>')
  .replace(/&amp;/g, '&');

const levels = [];
let current = null;

for (const rawLine of source.split('\n')) {
  const line = rawLine.replace(/\s+$/g, '');
  const titleMatch = line.match(/^Level\s+(\d+)\s*$/);

  if (titleMatch) {
    if (current) levels.push(current);
    current = { title: `Microban ${titleMatch[1]}`, rows: [] };
    continue;
  }

  if (!current) continue;
  if (line === '') continue;
  if (!/^[ #$@.*+]+$/.test(line)) {
    continue;
  }
  current.rows.push(line);
}

if (current) levels.push(current);

function outsideMask(rows) {
  const height = rows.length;
  const width = Math.max(...rows.map((row) => row.length));
  const grid = rows.map((row) => row.padEnd(width, ' '));
  const outside = Array.from({ length: height }, () => Array(width).fill(false));
  const queue = [];

  function enqueue(x, y) {
    if (x < 0 || y < 0 || x >= width || y >= height) return;
    if (outside[y][x] || grid[y][x] !== ' ') return;
    outside[y][x] = true;
    queue.push([x, y]);
  }

  for (let x = 0; x < width; x++) {
    enqueue(x, 0);
    enqueue(x, height - 1);
  }
  for (let y = 0; y < height; y++) {
    enqueue(0, y);
    enqueue(width - 1, y);
  }

  for (let i = 0; i < queue.length; i++) {
    const [x, y] = queue[i];
    enqueue(x + 1, y);
    enqueue(x - 1, y);
    enqueue(x, y + 1);
    enqueue(x, y - 1);
  }

  return outside;
}

function convertLevel(level) {
  const outside = outsideMask(level.rows);
  const width = Math.max(...level.rows.map((row) => row.length));
  const rows = level.rows.map((row, y) => {
    let converted = '';

    for (let x = 0; x < width; x++) {
      const ch = row[x] || ' ';

      if (ch === '#') converted += '#';
      else if (ch === '$') converted += 'b';
      else if (ch === '@') converted += 'p';
      else if (ch === '.') converted += '*';
      else if (ch === '*') converted += 'B';
      else if (ch === '+') converted += 'P';
      else if (ch === ' ') converted += outside[y][x] ? ' ' : '.';
      else throw new Error(`Unexpected character ${JSON.stringify(ch)} in ${level.title}`);
    }

    return converted.replace(/\s+$/g, '');
  });

  return {
    title: level.title,
    map: rows.join('\n'),
  };
}

const formatted = levels.map(convertLevel);

if (formatted.length < 150) {
  throw new Error(`Expected at least 150 levels, found ${formatted.length}`);
}

for (const level of formatted) {
  const playerCount = [...level.map].filter((ch) => ch === 'p' || ch === 'P').length;
  const boxCount = [...level.map].filter((ch) => ch === 'b' || ch === 'B').length;
  const goalCount = [...level.map].filter((ch) => ch === '*' || ch === 'B' || ch === 'P').length;

  if (playerCount !== 1) throw new Error(`${level.title} has ${playerCount} players`);
  if (boxCount !== goalCount) {
    throw new Error(`${level.title} has ${boxCount} boxes and ${goalCount} goals`);
  }
}

const body = [
  '// Generated by levels/format-microban.mjs from microbanText.html.',
  '// Standard Sokoban symbols are converted to Creature Sokoban symbols:',
  '// # wall, . floor, * goal, b box, p player, B box-on-goal, P player-on-goal.',
  'window.MICROBAN_LEVELS = [',
  ...formatted.map((level) => {
    return `  { title: ${JSON.stringify(level.title)}, map: \`${level.map}\` },`;
  }),
  '];',
  '',
].join('\n');

writeFileSync(outputPath, body);
console.log(`Wrote ${formatted.length} levels to ${outputPath.pathname}`);
